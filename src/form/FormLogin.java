    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = String.valueOf(txtPassword.getPassword());
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!", "Greska", JOptionPane.ERROR_MESSAGE);
        } else {
            if (btnKlijent.isSelected()) {
                try {
                    Klijent klijent = Kontroler.getInstance().loginKlijent(username, password);

                    setVisible(false);
                    Sesija.getInstance().setKorisnik(klijent);

                    frm = new FormMain();
                    frm.setVisible(true);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                try {
                    VlasnikSmestaja vlasnik = Kontroler.getInstance().loginVlasnik(username, password);

                    setVisible(false);
                    Sesija.getInstance().setKorisnik(vlasnik);
                    frm = new FormMain();
                    frm.setVisible(true);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Greska", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed


